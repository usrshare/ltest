#define arraylen(x) (sizeof(x)/sizeof(*x)) //for random name lists

int random_first_name(char* dest, enum entity_gender gender) {

	//by this point, gender should NOT be EG_RANDOM, EG_MBIAS or EG_FBIAS.
	//if it happens to be this way, though, this function will return
	//a gender-neutral name.

	enum entity_gender name_gender = EG_NEUTRAL;

	switch(gender) {
		case EG_MALE:
			if ((unsigned int)randval(arraylen(male_first_names) + arraylen(gender_neutral_first_names)) > arraylen(male_first_names)) name_gender = EG_NEUTRAL; else name_gender = EG_MALE; break;
		case EG_FEMALE:
			if ((unsigned int)randval(arraylen(female_first_names) + arraylen(gender_neutral_first_names)) > arraylen(female_first_names)) name_gender = EG_NEUTRAL; else name_gender = EG_FEMALE; break;
		case EG_WMPATRIARCH: name_gender = EG_WMPATRIARCH; break;
		default: name_gender = EG_NEUTRAL;
	}

	const char* randname;

	switch(name_gender) {
		case EG_MALE:
			randname = male_first_names[randval(arraylen(male_first_names))];
			break;
		case EG_FEMALE:
			randname = female_first_names[randval(arraylen(female_first_names))];
			break;
		case EG_WMPATRIARCH:
			randname = great_white_male_patriarch_first_names[randval(arraylen(great_white_male_patriarch_first_names))];
			break;
		case EG_NEUTRAL:
		default:
			randname = gender_neutral_first_names[randval(arraylen(gender_neutral_first_names))];
			break;
	}

	strncpy(dest,randname,16);
	return 0;
}

